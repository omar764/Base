package com.casa.pruebatecica;

/**
 *
 * @author HP
 */
public class WelcomeBean {

    private String item;
    private String nombre;
    private String cantidad;
    private String codInventario;
    /**
     * @return the item
     */
    public String getItem() {
        return item;
    }

    /**
     * @param item the item to set
     */
    public void setItem(String item) {
        this.item = item;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the cantidad
     */
    public String getCantidad() {
        return cantidad;
    }

    /**
     * @param cantidad the cantidad to set
     */
    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }

    /**
     * @return the codInventario
     */
    public String getCodInventario() {
        return codInventario;
    }

    /**
     * @param codInventario the codInventario to set
     */
    public void setCodInventario(String codInventario) {
        this.codInventario = codInventario;
    }  
    
}
